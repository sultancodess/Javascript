/*
Variables in JavaScript
There are two types of variables:
1. Constants
    - Declared using the "const" keyword
    - Example: const myName = "Sultan Alam";
2. Variables
    - Declared using "var" or "let" keywords
    - Example: 
      var myName = "Salman Khan";
      let myAge = 21;

Note: It is recommended to avoid using "var" due to issues with block scope and functional scope.
*/

/* Displaying the variables
1. Using console.log("string", value)
2. Using console.table([myName, myAge])
*/

const myName = "Sultan Alam";
let myAge = 21;

console.log("Name:", myName);
console.log("Age:", myAge);
console.table([myName, myAge]);


